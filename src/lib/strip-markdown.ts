/**
 * stripMarkdown — 将 Markdown 格式文本转为纯文字，适合 TTS 朗读。
 *
 * 处理规则（按顺序）：
 *  1. 代码块（```...```）→ 移除
 *  2. 行内代码（`...`）→ 保留代码内容
 *  3. 图片（![alt](url)）→ 移除
 *  4. 链接（[text](url)）→ 保留 text
 *  5. 粗体+斜体（***...***）→ 保留内容
 *  6. 粗体（**...**、__...__）→ 保留内容
 *  7. 斜体（*...*、_..._）→ 保留内容
 *  8. 删除线（~~...~~）→ 保留内容
 *  9. 标题（# ## ### ...）→ 保留标题文字
 * 10. 引用（> ...）→ 保留内容
 * 11. 有序/无序列表标记（- * + 1.）→ 移除标记
 * 12. 分隔线（--- *** ===）→ 移除
 * 13. 多余空行 → 合并为单空行
 * 14. 首尾空白 → trim
 */
export function stripMarkdown(text: string): string {
  return text
    // 代码块（含语言标识）
    .replace(/```[\s\S]*?```/g, "")
    // 行内代码
    .replace(/`([^`]+)`/g, "$1")
    // 图片
    .replace(/!\[[^\]]*\]\([^)]*\)/g, "")
    // 链接
    .replace(/\[([^\]]+)\]\([^)]*\)/g, "$1")
    // 粗体+斜体
    .replace(/\*{3}([^*]+)\*{3}/g, "$1")
    .replace(/_{3}([^_]+)_{3}/g, "$1")
    // 粗体
    .replace(/\*{2}([^*]+)\*{2}/g, "$1")
    .replace(/_{2}([^_]+)_{2}/g, "$1")
    // 斜体
    .replace(/\*([^*\n]+)\*/g, "$1")
    .replace(/_([^_\n]+)_/g, "$1")
    // 删除线
    .replace(/~~([^~]+)~~/g, "$1")
    // 标题（# 到 ######）
    .replace(/^#{1,6}\s+/gm, "")
    // 引用块
    .replace(/^>\s?/gm, "")
    // 无序列表标记（- * +）
    .replace(/^[\s]*[-*+]\s+/gm, "")
    // 有序列表标记（1. 2. ...）
    .replace(/^[\s]*\d+\.\s+/gm, "")
    // 分隔线
    .replace(/^[-*=]{3,}\s*$/gm, "")
    // 合并连续空行
    .replace(/\n{3,}/g, "\n\n")
    .trim();
}
